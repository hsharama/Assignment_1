# 1.  Takes two numbers as input from the user.
# 2.  Performs the basic mathematical operations on these two numbers:
# o	Addition
# o	Subtraction
# o	Multiplication
# o	Division

a = float(input("Enter 1st number: "))
b = float(input("Enter 2nd number: "))
c = a + b
print('sum is : ',round(c))


d = a - b
print('subtraction is : ',round(d))

e = a * b
print('multiplication is : ',round(e))

f = a / b
print('division is : ',round(f))

